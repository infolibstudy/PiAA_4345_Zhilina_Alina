#include <iostream>
#include <string>
#include <vector>
#include <fstream>

using namespace std;

int main() {
    // Инициализация файла для записи логов
    ofstream log("log2.txt");
    log << "--- Проверка на циклический сдвиг (алгоритм КМП) ---\n";

    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    string A, B;
    getline(cin, A);
    getline(cin, B);

    log << "Считана строка A: '" << A << "'\n";
    log << "Считана строка B: '" << B << "'\n\n";

    size_t n = A.size();
    size_t m = B.size();

    log << "Длина строки A (n) = " << n << "\n";
    log << "Длина строки B (m) = " << m << "\n\n";

    // циклический сдвиг возможен только при равной длине
    if (n != m) {
        log << "Длины строк не совпадают (n != m). Циклический сдвиг невозможен. Возврат -1.\n";
        log.close();
        cout << -1 << "\n";
        return 0;
    }

    // обработка пустых строк (обе пусты – сдвиг 0)
    if (n == 0) {
        log << "Обе строки пусты (n == 0). Сдвиг равен 0. Возврат 0.\n";
        log.close();
        cout << 0 << "\n";
        return 0;
    }

    // для проверки на циклический сдвиг будем искать B в удвоенной строке A
    string text = A + A;
    log << "Создана удвоенная строка A (text) для поиска: '" << text << "'\n\n";

    // построение префикс-функции для B
    log << "--- Построение префикс-функции для строки B ---\n";
    vector<size_t> pi(m, 0);
    log << "Инициализация массива pi нулями. Длина массива: " << m << "\n";

    for (size_t i = 1; i < m; ++i) {
        log << "\nИтерация i=" << i << ", текущий символ B[" << i << "] = '" << B[i] << "'\n";
        size_t j = pi[i - 1];
        log << "  Начальное значение j (из pi[" << i - 1 << "]): " << j << "\n";

        while (j > 0 && B[i] != B[j]) {
            log << "  Несовпадение: B[" << i << "] ('" << B[i] << "') != B[" << j << "] ('" << B[j] << "')\n";
            log << "  Откат j к pi[" << j - 1 << "]\n";
            j = pi[j - 1];
            log << "  Новое значение j = " << j << "\n";
        }
        if (B[i] == B[j]) {
            log << "  Совпадение: B[" << i << "] == B[" << j << "] ('" << B[i] << "'). Увеличиваем j.\n";
            ++j;
        } else {
            log << "  Совпадений нет, j остается равным " << j << "\n";
        }
        pi[i] = j;
        log << "  -> Записываем pi[" << i << "] = " << j << "\n";

        // Фиксация текущего вида массива префикс-функции
        log << "  Текущий массив pi: [";
        for (size_t k = 0; k < m; ++k) {
            log << pi[k] << (k == m - 1 ? "" : ", ");
        }
        log << "]\n";
    }

    // поиск первого вхождения B в text (КМП)
    log << "\n--- Поиск первого вхождения B в text (КМП) ---\n";
    size_t j = 0;
    log << "Начальная длина совпадения j = 0\n";

    for (size_t i = 0; i < text.size(); ++i) {
        log << "\nИтерация по тексту i=" << i << ", текущий символ text[" << i << "] = '" << text[i] << "'\n";
        log << "  Текущая длина совпадения j = " << j << "\n";

        while (j > 0 && text[i] != B[j]) {
            log << "  Несовпадение: text[" << i << "] ('" << text[i] << "') != B[" << j << "] ('" << B[j] << "')\n";
            log << "  Откат j к pi[" << j - 1 << "]\n";
            j = pi[j - 1];
            log << "  Новое значение j = " << j << "\n";
        }
        if (text[i] == B[j]) {
            log << "  Совпадение: text[" << i << "] == B[" << j << "] ('" << text[i] << "'). Увеличиваем j.\n";
            ++j;
        } else {
            log << "  Совпадений нет, j остается равным " << j << "\n";
        }
        if (j == m) {
            // Вхождение найдено, печатаем результат
            log << "  !!! Найдено полное вхождение строки B (j == " << m << "). Индекс начала (сдвиг): " << (i - m + 1) << "\n";
            log << "--- Завершение алгоритма (успех) ---\n";
            log.close();
            cout << i - m + 1 << "\n";
            return 0;
        }
    }
    // Вхождение не найдено
    log << "\nВхождение строки B в text не найдено.\n";
    log << "--- Завершение алгоритма (неудача) ---\n";
    log.close();
    cout << -1 << "\n";
    return 0;
}
